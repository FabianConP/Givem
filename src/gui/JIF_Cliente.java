    private void jb_FindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_FindActionPerformed
        Conexion c;
        DefaultTableModel dtm = (DefaultTableModel) jtb_Clientes.getModel();
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        jtb_Clientes.setModel(dtm);
        try {
            c = new Conexion();
            try (Statement s = c.c.createStatement()) {
                if (jrb_Cedula.isSelected()) {
                    String cedula = jt_Buscar.getText();
                    if (cedula.equals("")) {
                        cedula = "0";
                    }
                    int fcedula = Integer.parseInt(cedula);
                    String sentence = "select idNumero, nombre, pais, ciudad, direccion, telefono, correoElectronico from tb_cliente, tb_contacto where tb_contacto.`idUbicacion` = tb_cliente.`contacto_idUbicacion` and idNumero = " + fcedula + "";
                    ResultSet r = s.executeQuery(sentence);
                    while (r.next()) {
                        Object[] o = new Object[]{r.getString(1), r.getString(2), r.getString(3),
                            r.getString(4), r.getString(5), r.getString(6), r.getString(7)};

                        dtm.addRow(o);
                        jtb_Clientes.setModel(dtm);
                    }
                } else if (jrb_Nombre.isSelected()) {
                    String nombre = jt_Buscar.getText();
                    String sentence = "select idNumero, nombre, pais, ciudad, direccion, telefono, correoElectronico from tb_cliente, tb_contacto where tb_contacto.`idUbicacion` = tb_cliente.`contacto_idUbicacion` and nombre like '%" + nombre + "%'";
                    ResultSet r = s.executeQuery(sentence);
                    while (r.next()) {
                        Object[] o = new Object[]{r.getString(1), r.getString(2), r.getString(3),
                            r.getString(4), r.getString(5), r.getString(6), r.getString(7)};

                        dtm.addRow(o);
                        jtb_Clientes.setModel(dtm);
                    }
                }
                s.close();
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jb_FindActionPerformed

