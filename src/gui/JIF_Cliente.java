    private void jb_BorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_BorrarActionPerformed
        String idNumero = jt_Cedula.getText();
        if (idNumero.length() == 0) {
            JOptionPane.showMessageDialog(this, "Escriba la cédula del cliente, está vacío.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            int op = JOptionPane.showConfirmDialog(this, "¿Está seguro que desea borrar el\ncliente asociado a la cédula " + idNumero + " ?");
            if (op == JOptionPane.YES_OPTION) {
                try {
                    Conexion c = new Conexion();
                    Statement s = c.c.createStatement();
                    String sentence = "CALL sp_borrarCliente(" + idNumero + ")";
                    ResultSet r = s.executeQuery(sentence);
                    if (r.next()) {
                        String ans = r.getString(1);
                        JOptionPane.showMessageDialog(this, ans);
                    } else {
                        JOptionPane.showMessageDialog(this, "Error al intentar borrar", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                    c.c.close();
                } catch (ClassNotFoundException | SQLException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
                llenarTablaCliente();
                jt_Cedula.setText("");
                jt_Nombre.setText("");
                jt_Pais.setText("");
                jt_Ciudad.setText("");
                jt_Direccion.setText("");
                jt_Telefono.setText("");
                jt_Correo_Electronico.setText("");
            }
        }

    }//GEN-LAST:event_jb_BorrarActionPerformed

