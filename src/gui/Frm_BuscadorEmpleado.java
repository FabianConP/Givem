    private void llenarTablaEmpleado(){
        DefaultTableModel dtm = (DefaultTableModel) jta_Empleado.getModel();
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        jta_Empleado.setModel(dtm);
        try {
            Conexion c = new Conexion();
            Statement s = c.c.createStatement();
            String tipoFiltro = (jrb_Codigo.isSelected()) ? "Codigo" : "Nombre";
            String valorFiltro = jt_Filtro.getText();
            String sentence = "CALL sp_BuscadorEmpleado(\"" + tipoFiltro
                    + "\",\"" + valorFiltro + "\")";
            ResultSet r = s.executeQuery(sentence);
            while (r.next()) {
                Object[] o = new Object[]{r.getString(1), r.getString(2), r.getString(3)};
                dtm.addRow(o);
                jta_Empleado.setModel(dtm);
            }
            c.c.close();
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
